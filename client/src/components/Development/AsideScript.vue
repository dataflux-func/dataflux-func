      this.expandedNodeMap = this.$store.state.asideScript_expandedNodeMap || {};
      this.defaultExpandedNodeKeys = Object.keys(this.expandedNodeMap);

      // Select automatically
      this.selectNode(this.$route.params.id, { scroll: true });
