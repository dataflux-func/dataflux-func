        this.common.appendSearchFields(scriptMap[d.id], ['id', 'title'])

        // Add to parent layer's "children" field
        if (scriptSetMap[d.scriptSetId]) {
          scriptSetMap[d.scriptSetId].children.push(scriptMap[d.id]);
