            'title',
            'description',
            'definition',
            'scriptSetId',
            'scriptId',
            'sset_type',
            'integration',
            'extraConfigJSON',
          ]
        },
      });
      if (!apiRes || !apiRes.ok) return;

      window._DFF_funcIds = [];
      apiRes.data.forEach(d => {
        // Skip if Script not exists
        if (!scriptMap[d.scriptId]) return;

        window._DFF_funcIds.push(d.id);

        // Get parent Script
