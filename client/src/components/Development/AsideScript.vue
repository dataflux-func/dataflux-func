        let lockedByUser    = d.lockedByUserName || d.lockedByUsername || this.$t('UNKNOWN');
        let isLockedByMe    = d.lockedByUserId === this.$store.getters.userId;
        let isLockedByOther = d.lockedByUserId && !isLockedByMe;
        scriptMap[d.id] = {
