<template>
  <el-dialog
    id="ScriptSetLockConfig"
    :visible.sync="show"
    :close-on-click-modal="false"
    :close-on-press-escape="false"
    width="750px">
    <template slot="title">
      {{ $t('Lock Script Set') }} <code class="text-main">{{ data.title || data.id }}</code>
    </template>

    <el-container direction="vertical">
      <el-main>
        <div class="setup-form">
          <el-form ref="form" label-width="65px" :model="form" :disabled="!can([ 'scriptSet_setup' ])" :rules="formRules">
            <el-form-item>
              <InfoBlock v-if="isLockedByMe" type="success" :title="$t('This Script is locked by you')" />
              <InfoBlock v-else-if="isLockedByOther" :type="can([ 'scriptSet_setup' ]) ? 'warning' : 'error'" :title="$t('This Script is locked by other user ({user})', { user: lockedByUser })" />
            </el-form-item>

            <el-form-item class="setup-divider">
              <el-divider content-position="left"><h1>{{ $t('Allow Others to Operate') }}</h1></el-divider>
            </el-form-item>

            <template v-for="(member, index) in form.lockConfigJSON.members || []">
              <el-form-item
                class="lock-config-members"
                :label="`#${index + 1}`"
                :key="`lock-config-member-user-id-${index}`"
                :prop="`lockConfigJSON.members.${index}.userId`"
                :rules="formRules_userId">
                <el-select
                  :placeholder="$t('Select User...')"
                  :no-data-text="$t('No Data')"
                  filterable
                  v-model="member.userId">
                  <el-option-group>
                    <el-option :label="$t('All Users')" key="_ALL" value="_ALL"></el-option>
                  </el-option-group>
                  <el-option-group>
                    <el-option
                      v-for="item in users"
                      :key="item.id"
                      :label="item.name || item.username"
                      :value="item.id">
                      <span class="select-item-name">
                        <template v-if="item.name">{{ item.name }} / </template><code class="code-font">{{ item.username }}</code>
                      </span>
                      <code class="select-item-id code-font">ID: {{ item.id }}</code>
                    </el-option>
                  </el-option-group>
                </el-select>

                <!-- Delete button -->
                <el-link type="danger" class="lock-config-members-button right-button" @click.prevent="removeMember(index)">{{ $t('Delete') }}</el-link>
              </el-form-item>

              <el-form-item
                class="lock-config-members"
                :key="`lock-config-member-allowed-operations-${index}`"
                :prop="`lockConfigJSON.members.${index}.allowedOperations`"
                :rules="formRules_allowedOperations">
                <el-select
                  :placeholder="$t('Allowed Operations...')"
                  :no-data-text="$t('No Data')"
                  filterable
                  multiple
                  v-model="member.allowedOperations">
                  <el-option :label="$t('Read Script Code, Clone and Export Script Set')" value="scriptSet_readScriptCode"></el-option>
                  <el-option :label="$t('Read, Add and Edit Script Code, Clone and Export Script Set')" value="scriptSet_editScriptCode"></el-option>
                  <el-option :label="$t('Setup Script Set')" value="scriptSet_setup"></el-option>
                  <el-option :label="$t('Setup Script')" value="scriptSet_setupScript"></el-option>
