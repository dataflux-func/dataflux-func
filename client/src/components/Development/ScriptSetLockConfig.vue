<script>
export default {
  name: 'ScriptSetLockConfig',
  components: {
  },
  watch: {
    show(val) {
      if (val && this.$refs.form) {
        this.$refs.form.clearValidate();
      }
    },
  },
  methods: {
    async loadData(id) {
      this.data.id = id;

      let apiRes = await this.T.callAPI_getOne('/api/v1/script-sets/do/list', this.scriptSetId, {
        query: {
          fields: [
            'id',
            'title',
            'lockedByUserId',
            'lockConfigJSON',
            'lockedByUserUsername',
            'lockedByUserName',
            'lockConfigMemberAllowMap',
          ]
        }
      });
      if (!apiRes || !apiRes.ok) return;

      this.data = apiRes.data;

      // Ensure lockConfigJSON havs a value
      this.data.lockConfigJSON = this.data.lockConfigJSON || {};

      let nextForm = {};
      Object.keys(this.form).forEach(f => nextForm[f] = this.data[f]);
      this.form = nextForm;

      // Load user list
      apiRes = await this.T.callAPI_get('/api/v1/users/do/list', {
        query: {
          fields: [
            'id',
            'name',
            'username',
          ],
        },
      });
      if (!apiRes || !apiRes.ok) return;
      this.users = apiRes.data;

      this.show = true;
    },
    async submitData(operation) {
      try {
        await this.$refs.form.validate();
      } catch(err) {
        return console.error(err);
      }

      let dataId = null;
      switch(operation) {
        case 'lock':
          dataId = await this.lockData();
          break;

        case 'unlock':
          dataId = await this.unlockData();
          break;
      }

      if (dataId) {
        this.$store.commit('updateEditor_selectedItemId', null);
      }
    },
    async lockData() {
      let _formData = this.T.jsonCopy(this.form);
      delete _formData.id;

      _formData.isLocked = true;

      let apiRes = await this.T.callAPI('post', '/api/v1/script-sets/:id/do/modify', {
        params  : { id: this.scriptSetId },
        body    : { data: _formData },
        feedback: { okMessage: this.isLockedByMe ? this.$t('Script Set lock config updated')
                                                 : this.$t('Script Set locked') },
      });
      if (!apiRes || !apiRes.ok) return;

      this.$store.commit('updateScriptListSyncTime');
      this.show = false;

      return this.scriptSetId;
    },
    async unlockData() {
      if (!await this.T.confirm(this.$t('Are you sure you want to unlock the Script Set and delete all the lock configs?'))) return;

      let apiRes = await this.T.callAPI('post', '/api/v1/script-sets/:id/do/modify', {
        params  : { id: this.scriptSetId },
        body    : { data: { isLocked: false } },
        feedback: { okMessage: this.$t('Script Set unlocked') },
      });
      if (!apiRes || !apiRes.ok) return;

      this.$store.commit('updateScriptListSyncTime');
      this.show = false;

      return this.scriptSetId;
    },

    addMember() {
      if (this.T.isNothing(this.form.lockConfigJSON.members)) {
        this.$set(this.form.lockConfigJSON, 'members', []);
      }
      this.form.lockConfigJSON.members.push({
        userId           : '',
        allowedOperations: [],
      });
    },
    removeMember(index) {
      this.form.lockConfigJSON.members.splice(index, 1);
    },
    can(operation) {
      return this.common.lockConfigCan(this.lockedByUserId, this.data.lockConfigMemberAllowMap, operation);
    },
  },
  computed: {
    scriptSetId() {
      return this.data.id;
    },

    lockedByUserId() {
      return this.data.lockedByUserId;
    },
    lockedByUser() {
      return this.data.lockedByUserName || this.data.lockedByUsername;
    },
    isLockedByMe() {
      return this.lockedByUserId === this.$store.getters.userId;
    },
    isLockedByOther() {
      return this.lockedByUserId && !this.isLockedByMe;
    },
  },
  props: {
  },
  data() {
    return {
      show: false,

      data : {},
      users: [],

      form: {
        lockConfigJSON: [],
      },
      formRules: {
      },
      formRules_userId: {
        trigger: 'blur',
        message : this.$t('Please select User'),
        required: true,
      },
      formRules_allowedOperations: {
        trigger: 'blur',
        message : this.$t('Please select Allowed Operations'),
        required: true,
      },
    }
  },
}
</script>

